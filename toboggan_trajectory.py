# Class position with three arguements: x, y coordinates and trees

def tree_checker(map, slope = [3,1]):

    trees = 0
    x = 0
    y = 0

    ptrn_length = len(map[0])
    ptrn_height = len(map)

    while y < ptrn_height:

        # Is there a tree in the way
        if map[y][x] == "#":
            trees += 1

        # Moving to next position
        x += slope[0]
        y += slope[1]

        if x >= ptrn_length:
            x -= ptrn_length

    return(trees)

print(tree_checker(map))

multi_ans = tree_checker(map, [1,1])*tree_checker(map, [3,1])*tree_checker(map, [5,1])*tree_checker(map, [7,1])*tree_checker(map, [1,2])

print(multi_ans)